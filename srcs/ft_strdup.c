#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	int		i;

	if (s[0] != '\0')
		dup = malloc(ft_strlen(s) + 1 * sizeof(char));
	else
		dup = malloc(sizeof(char));
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dup[i] = s[i];
		++i;
	}
	dup[i] = '\0';
	return (dup);
}
